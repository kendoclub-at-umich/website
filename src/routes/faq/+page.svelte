<h1>Frequently Asked Questions</h1>

<hr />

<details>
	<summary>This looks dangerous. Will I get hurt or injured?</summary>
	<p>
		As with any sport, there is some level of risk involved. However, injuries are quite rare in
		Kendo. The bamboo swords (shinai) and armor (bogu) are designed to absorb impact and protect
		you.
	</p>
	<p>
		Before you put on bogu, we will teach you how to protect yourself and others from accidental
		harm. It is an important part of Kendo to fight with respect and gratitude rather than force and
		anger, so Kendo players always put their opponent’s safety above winning a sparring match.
		Safety routines in kendo training include regular inspection of shinai and bogu and proper
		stretching before practice.
	</p>
	<p>
		The most common injury is your opponent missing the correct target, which can sting a little and
		possibly bruise.
	</p>
</details>

<hr />

<details>
	<summary>How much does Kendo cost?</summary>
	<p>
		Costs vary greatly from person to person. Most beginners spend around $200 per year. Around a
		year in, most club members spend about $500 on their first set of bogu (armor). Some advanced
		members spend upwards of $300 a year.
	</p>
</details>

<hr />

<details>
	<summary>Where do I buy a bogu set and how do I find the right size?</summary>
	<p>
		Consult the instructors first (you need their approval for wearing bogu anyway). They will help
		you with measurements and direct you to bogu vendors we have had good experiences with.
		Depending on the order, the club might have a shortcode discount or could arrange group orders.
	</p>
</details>

<hr />

<details>
	<summary>Do you use real swords?</summary>
	<p>
		No. Kendo uses 2 types of swords: the bamboo sword (shinai) for general practices and
		competitions, and the wooden sword (bokuto) to more closely approximate the shape and weight of
		a sword when practicing forms (Kata).
	</p>
	<p>
		During some Kata demonstrations we may use metal swords, but because of the possibility of
		injury this is not common.
	</p>
</details>

<hr />

<details>
	<summary>Do you go to competitions/tournaments?</summary>
	<p>
		Yes. Kendo is a martial art with an active competition component, and tournament is one of the
		best ways to meet new friends. Our club competes in 3-4 tournaments per year in local areas. Our
		awesome student officers also organize an internal tournament within our club every year!
	</p>
</details>

<hr />

<details>
	<summary>Do Kendo instructors get paid?</summary>
	<p>
		No. Our instructors teach us as volunteers. Dues collected by our student officers are for room
		booking and social activities in the club.
	</p>
</details>

<hr />

<details>
	<summary>Do I need to be able to speak Japanese?</summary>
	<p>
		No. Although there are Japanese terms in Kendo, we teach in English. You will become familiar
		with the use of Japanese terms over time with repetition.
	</p>
</details>

<hr />

<details>
	<summary> How do you increase your rank? </summary>
	<p>
		Kendo uses a Kyu/Dan ranking system. Adult Kendo learners start from 2-Kyu or 1-Kyu, then
		advance to Dan ranks. Although Dan ranks are considered “black belts”, we do not wear belts or
		any other outward sign of ranking.
	</p>
	<p>
		How long it takes to progress depends on your dedication and availability. The first stage is to
		develop fundamentals, which may take weeks or months. The second stage is to put on bogu and get
		used to it. Once you are able to perform good fundamentals in bogu, you will get permission from
		our instructors to test for Kyu. The third stage is to perfect your fundamentals, learn advance
		techniques, and work towards your first Dan rank.
	</p>
	<p>
		It commonly takes 1-2 semesters to be ready to start wearing bogu, and no less than 2 years of
		regular practice to reach 1-Dan (the first "black belt" rank). There is a mandatory waiting
		period to take the test for the next Dan rank that increases at each subsequent rank. 1-Dan to
		2-Dan is a 1 year wait, 2-Dan to 3-Dan is a 2 year wait, 3-Dan to 4-Dan is a 3 year wait, and so
		on.
	</p>
</details>

<hr />

<details>
	<summary>I have no experience, nor am I physically fit. Will that be a problem?</summary>
	<p>
		No, it won’t be a problem. Kendo does not have any distinctions between young or old, men or
		women, athletic or not. As a university club, we are especially dedicated to training beginners.
	</p>
	<p>
		Our practices can be challenging, but they will always also be self-paced for safety. Keep an
		open mind, as you will find your physical limitations challenged. You decide how far you wish to
		push your own limits.
	</p>
</details>

<hr />

<details>
	<summary>
		I have a high ranking belt in another martial art, can it be transferred to Kendo?
	</summary>
	<p>
		We start everyone with the same basic training in Kendo to build strong fundamentals. However,
		prior martial arts experience might help you advance faster in Kendo.
	</p>
	<p>
		If you come from a Kumdo background, you may be considered to hold a comparable rank in Kendo.
		Please talk with the instructors and bring documentation of your current rank for verification.
	</p>
</details>

