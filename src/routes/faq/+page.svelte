<details>
	<summary>Do you use real swords?</summary>
	<p>
		No. Kendo uses 2 types of swords: (1) the bamboo sword (shinai) for general practice and
		competition, (2) the wooden sword (bokuto) to more closely approximate the shape and weight of a
		sword when practicing forms (Kata).
	</p>
	<p>
		During some Kata demonstrations we may use metal swords, but because of the possibility of
		injury this is not common.
	</p>
</details>

