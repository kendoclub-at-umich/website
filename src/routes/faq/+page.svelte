<details>
	<summary>
		How long will it take to earn a black belt and beyond? How to get rank in Kendo?
	</summary>
	<p>
		Kendo uses a Kyu/Dan ranking system. Adult Kendo leaners start from 2 Kyu or 1 Kyu, then advance
		to Dan ranks. Although Dan ranks are considered “black belts”, we do not wear belts or any other
		outward sign of ranking.
	</p>
	<p>
		The time to get ranks depending on your dedication and availability. The first stage is to
		develop fundamentals, which may take weeks or months. The second stage is to put on bogu and get
		used to it. Once you are able to perform good fundamentals in bogu, you will get permission from
		our instructors to test for Kyu. The third stage is to perfect your fundamentals, learn advance
		techniques, and work towards your first Dan rank.
	</p>
	<p>
		It commonly takes 1-2 semesters to put on bogu, no less than 2 years of regular practice to
		reach 1 Dan. There is a mandatory waiting period for dan ranks that increases for each new
		level. 1 Dan to 2 Dan is a 1 year wait, 2 Dan to 3 Dan is a 2 year wait, 3 Dan to 4 Dan is a 3
		year wait and so on.
	</p>
</details>

<hr />

<details>
	<summary>I have no experience, nor am I really physically fit. Will that be a problem?</summary>
	<p>
		It won’t be a problem. Kendo does not have any distinctions between young or old, men or women,
		athletic or not. As a university club, we are especially dedicated to training beginners.
	</p>
	<p>
		Our practices can be challenging, but they will always also be self-paced for safety. Keep an
		open mind as you will find your physical limitations challenged, but you will decide how far you
		wish to push you own limits.
	</p>
</details>

<hr />

<details>
	<summary>
		I have a high ranking belt in another martial art, can it be transferred to Kendo?
	</summary>
	<p>
		Although many martial arts share similar fundamentals, you will start at a similar introduction
		level in Kendo. Your training in other martial arts might help you advance faster in Kendo.
	</p>
	<p>
		If you come from a Kumdo background, you may be considered to hold a comparable rank in Kendo.
		Please talk with the instructors and bring documentation of your current rank for verification.
	</p>
</details>

