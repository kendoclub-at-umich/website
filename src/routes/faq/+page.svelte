<details>
	<summary>Do you go to competitions/tournaments?</summary>
	<p>
		Yes. Kendo is a martial art with an active competition component, and tournament is one of the
		best ways to meet Kendo friends. Our club members compete in 3-4 tournaments per year in local
		areas. Our awesome student officers also organize an internal tournament within our club every
		year!
	</p>
</details>

