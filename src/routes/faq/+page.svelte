<h1>Frequently Asked Questions</h1>

<hr />

<details>
	<summary>It looks dangerous, will I get hurt or injured?</summary>
	<p>
		As with any sports, there is some level of risk involved. But in general, injuries are quite
		rare for Kendo players. Our bamboo sword (shinai) and armor (bogu) are designed to absorb
		impact, and have been perfected for over 300 years. If your opponent misses the correct target,
		it can sting a little.
	</p>
	<p>
		Before you put on bogu, we will teach you how to protect yourself and others from accidental
		harm. It is an important part of Kendo to fight with respect and gratitude instead of with
		muscle and anger, so Kendo players always put their opponents’ safety prior to winning a
		sparing.
	</p>
	<p>
		Safety routine in kendo training includes regular inspection of shinai and bogu, proper
		stretching before practice, etc.
	</p>
</details>

<hr />

<details>
	<summary>Is Kendo an expensive hobby?</summary>
	<p>In general, the cost of practicing Kendo includes:</p>
	<ol>
		<li>Club dues and gym pass (see home page).</li>
		<li>
			AUSKF membership (<a href="https://www.auskf.org/">https://www.auskf.org/</a>):<br />
			The AUSKF (All United States Kendo Federation) membership is ~ $80 per year. Students get discount.
			Beginners do not need to pay until putting on bogu.
		</li>
		<li>
			Promotion test fees and tournament fees:<br />
			As a reference, in 2024, for Kyu test it’s around $40 (test) + $30 (certificate); for 1-Dan (black
			belt entry level) it’s around $50 (test) + $50 (certificate). It goes higher for higher ranks.
			Tournament registration fee varies, typically $30 - $70.
		</li>
		<li>
			Shinai (bamboo sword) and bokuto (wooden sword):<br />
			Depending on practice frequency, a shinai ($30 - $60) can last several months (4-12 month). There
			are ways to reduce the cost by combining orders to get discounts, or making franken-shinai with
			the intact slats from broken shinais. Bokken (~ $50 and up) is only need for promotion test and
			typically never breaks.
		</li>
		<li>
			Bogu (armor):<br />
			Bogu is probably <strong>the most expensive component</strong>, but it can last decades with
			good maintenance. A good beginner set can be ~ $400 and up. Always ask around as our
			instructors and senior members might have discount information for some vendors. Also, our
			club might have loaner bogu sets for rent.<br />
			<br />
			Beginners will not wear bogu until they have learned some solid fundamentals, so you don’t need
			to worry about this in the beginning.
		</li>
	</ol>
</details>

