<h1>Parts of a Shinai and How to Hold It</h1>

<h2>Anatomy of a Shinai</h2>

<div class="grid-container">
	<figure>
		<enhanced:img
			src="./deconstructed-shinai.jpg"
			sizes="min(100px, 100vw)"
			alt="In black and white, parts of a shinai laid out on the floor."
		/>
		<figcaption>A deconstructed shinai</figcaption>
	</figure>

	<p>
		A shinai is generally comprised of four slats of bamboo, a leather cap, a plastic plug, a thin
		leather strap, a handle, a very small square of very thin steel, twine, a cross guard, and the
		cross guard support. As you can tell from the age of the picture used, the overall design and
		components of the shinai have not changed dramatically over the years. Although familiarity with
		the shinai is not required for promotion testing, it is a very important safety factor in your
		Kendo practice. We request that every member take the time to become familiar with how to take
		apart, adjust, and reassemble the shinai. This enables you to repair or replace faulty or
		damaged pieces. A damaged shinai is not only a bad reflection of you as a kendo practitioner,
		but also a potentially serious threat to your fellow senpai and kouhai. Ask one of your senpai
		how to take it apart and inspect the pieces. You will eventually want to take the shinai apart
		so that you can adjust the cord as the leather handle begins to stretch. This is also an
		excellent opportunity to adjust the handle length if needed.
	</p>
</div>

<p>
	Here are some terms that refer to parts of a shinai. If you become familiar with them, you'll have
	a common reference for discussion which can greatly reduce confusion in conversation.
</p>

<div class="grid-container">
	<figure>
		<enhanced:img
			src="./shinai-diagram-japanese-labels.jpg"
			sizes="min(100px, 100vw)"
			alt="Diagram of two shinai labelled in japanese."
		/>
		<figcaption>
			First shinai has its "striking side" facing down. Second shinai has it facing left.
		</figcaption>
	</figure>

	<dl>
		<dt>Kissaki: 切先【けんせん】</dt>
		<dd>Thrusting surface of the shinai. Tip of the sword.</dd>

		<dt>Sakigawa: 先革【さきがわ】</dt>
		<dd>Leather cap that covers the tip.</dd>

		<dt>Monouchi: 物打【ものうち】</dt>
		<dd>
			Valid striking area of the shinai. It includes the area from the very tip of the sword to the
			first edge of the nakayui. Since the shinai only has a single blade, this region only applies
			to the face of the shinai. You may not achieve a point hitting someone with the side of the
			shinai, even if it is in the monouchi.
		</dd>

		<dt>Nakayui: 中結【なかゆい】</dt>
		<dd>Narrow leather strap indicating the end of the shinai's striking/cutting surface.</dd>

		<dt>Chikama: 近間【ちかま】</dt>
		<dd>Any point below the nakayui to the tsuba which is not a valid scoring surface.</dd>

		<dt>Tsuru: 弦【つる】</dt>
		<dd>
			Twine or strong cord that denotes the back of the shinai. This string also holds most of the
			major components in place.
		</dd>

		<dt>Tsuba: 鍔【つば】</dt>
		<dd>
			Cross guard or hand guard of the shinai. Unlike western style guards, this is generally round
			and small.
		</dd>

		<dt>Tsubadome: 鍔止め【つばどめ】</dt>
		<dd>Supporting piece that holds the tsuba in place so it does not travel during use.</dd>

		<dt>Tsuka: 柄【がら】</dt>
		<dd>Handle, generally made of leather, that acts as the grip for the shinai.</dd>

		<dt>Tsukagashira: 柄頭【つかがしら】</dt>
		<dd>
			End of the shinai, generally referred to as a pommel. The bottom edge of your left hand wraps
			around here while holding the shinai.
		</dd>
	</dl>
</div>

<h2>How to Correctly Hold a Shinai</h2>

<h3>Practice Exercise</h3>

<div class="grid-container side-by-side-img">
	<figure>
		<enhanced:img
			src="./arm-position-front-view.png"
			sizes="min(100px, 100vw)"
			alt="Woman in kendo uniform facing forward, demonstrating arm position."
		/>
	</figure>

	<figure>
		<enhanced:img
			src="./arm-position-side-view.png"
			sizes="min(100px, 100vw)"
			alt="Woman in kendo uniform facing right, demonstrating arm position."
		/>
	</figure>
</div>

<p>
	Lets begin this portion of the discussion with an exercise. Stand up with your hands at your belt
	level. Place your open hands together as if you were going to pray. Put your fingertips all
	together. Your arms should be comfortably relaxed and naturally bent at the elbows. Keep your
	shoulders and elbows relaxed.
</p>

<div class="grid-container side-by-side-img">
	<figure>
		<enhanced:img
			src="./arm-position-side-view-hands-apart.png"
			sizes="(min-width:1280px) 400px, (min-width:450px) 350px, (min-width:0px) 200px"
			alt="Woman in kendo uniform facing right, demonstrating arm position. Hands apart showing where to place on a shinai."
		/>
	</figure>

	<figure>
		<enhanced:img
			src="./left-hand-shinai-grip.png"
			sizes="(min-width:1280px) 400px, (min-width:450px) 350px, (min-width:0px) 200px"
			alt="Close up of left hand correctly holding the shinai handle at the very bottom. Nails are painted a pearly pink."
		/>
	</figure>
</div>

<p>
	Now move you right hand ahead of your left hand with a fist's distance between them. The shinai
	goes into the left hand. This positioning is very important.
</p>

<div class="grid-container">
	<figure>
		<enhanced:img
			src="./both-hands-shinai-grip-side-view.png"
			sizes="min(100px, 100vw)"
			alt="Woman in kendo uniform facing right, holding a shinai correctly. Left hand is at the bottom of the shinai handle. Right hand is directly under the hand guard."
		/>
	</figure>

	<p>
		Note how, in the images, the left hand comes over the edge slightly and that the handle is
		actually at an angle in the hand. We do not hold a shinai as you would a flashlight or a
		baseball bat. The angle allows us to strike at the top of our opponents men and also to have
		greater range in our attack. Also notice that the hand is not tightly clenched. Only the final
		two fingers are exerting pressure on the handle.
	</p>
</div>

<h3>Common Mistakes</h3>

<div class="grid-container">
	<figure>
		<enhanced:img
			src="./right-hand-shinai-grip-and-mistakes.jpg"
			sizes="min(100px, 100vw)"
			alt="Two examples of right hand grip, one correct and one incorrect, each shown with and without a shinai."
		/>
	</figure>

	<p>
		The left hand grip is displayed here. The picture is from the other side for clarity. Correct
		positioning is marked with an “O” where common mistakes are marked with the “X”. As with the
		left hand, the right hand is not clenched tightly either. Notice in the second picture where the
		fingers are shown to be relaxed, but not open.
	</p>
</div>

<div class="grid-container">
	<figure>
		<enhanced:img
			src="./both-hands-shinai-grip-top-view.jpg"
			sizes="min(100px, 100vw)"
			alt="View from above of gripping a shinai, as if looking down at one's own hands. Dotted lines show a V shape made between the thumb and index finger of each hands, and the V shapes lining up along the shinai handle."
		/>
	</figure>

	<div>
		<p>
			At any point during cutting, you should be able to open your fingers and they should be
			pointing directly away from you at every moment in cutting.
		</p>

		<p>
			In bogu it is difficult to see correct placement of the hands, because of the kote. But your
			cuts will reflect to everyone else if you positioning is incorrect. It is important to always
			be aware of correct grip on the shinai. Check yourself, until it becomes second nature to you.
		</p>
	</div>
</div>

<div class="grid-container">
	<figure>
		<enhanced:img
			src="./both-hands-shinai-grip-mistakes.jpg"
			sizes="min(100px, 100vw)"
			alt="Three examples of incorrect shinai grips, two from above and one from the side. First and second examples show baseball bat grips. Third example shows left hand a few inches up from the end of shinai handle."
		/>
	</figure>

	<p>
		Make every effort to avoid gripping the shinai as shown in the pictures of common mistakes. They
		are all excellent examples of what not to do. When gripping the shinai correctly, you will
		naturally develop a strong kamae and be much less open to attacks.
	</p>
</div>

<h3>Point Your Shinai</h3>
<div class="grid-container">
	<figure>
		<enhanced:img
			src="./both-hands-shinai-grip-in-bogu-front-view.jpg"
			sizes="min(100px, 100vw)"
			alt="Kendo practitioner in full armor, holding a shinai and facing forward. Demonstrates the angle of the shinai and where the tip points."
		/>
	</figure>

	<p>
		Now that your hands are gripping the shinai properly, let's focus on the final aspect of holding
		the shinai. The tip of the shinai, the kissaki, is always pointed toward your opponent's throat.
		If the two of you walked directly towards each other, your kissaki would end up touching each
		other's throat. When practicing alone, picture a perfect reflection of yourself as your target
		and adjust the height of the tip of the shinai accordingly. It can help to practice facing a
		mirror.
	</p>
</div>

