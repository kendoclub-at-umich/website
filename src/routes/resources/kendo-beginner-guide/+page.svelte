<h2>Preliminary Steps And Premises</h2>
<p>
	If you are the first one to the practice, that is fantastic, get the broom or find a broom to
	sweep the floor clear of any debris or dust. A clean dojo makes for a much more successful
	practice and reduces the chance for injury. If there is more than one person, then take turns
	running the broom up and down the floor then pass it along to the next person. This lets everyone
	pitch in and still have time to get ready/changed. No one person should ever be cleaning the floor
	while others stand by and watch. Cleaning our practice space should always be a team effort. This
	is common courtesy and a part of Rei.
</p>
<p>
	Wearing the Keiko-gi and Hakama properly; in the beginning training in comfortable clothes that
	allow freedom of movement is encouraged, but you are also welcome to begin wearing uniform as soon
	as you feel comfortable. We will help you to get the correct size uniform and also how to wear and
	care for it. Remember that the practice of Kendo is done bare foot, so maintain good hygiene and
	short toe nails for everyone’s benefit. This is common courtesy and a part of Rei.
</p>
<p>
	Check equipment for damage before each and every practice, especially the Shinai. A splintered
	Shinai is dangerous, especially to your opponent. If it is damaged or you are not certain, ask
	someone senior to check it with you. It is always a good idea to have a spare Shinai with you so
	you don’t have to miss out on practice while making repairs you missed spotting earlier.
</p>
<p>
	Always keep your equipment and yourself neat and tidy. Check your physical condition and health
	regularly. We can’t reinforce this enough, but maintain your proper hygiene. Learn how to wash
	Keiko-gi and Hakama. When you are in bogu, take care of it - so it can take care of you. If there
	is something you don’t know or understand, be in charge of your success and ask. This is common
	courtesy and a part of Rei.
</p>
<p>
	The Shinai represents a sword (Katana, Ken); treat it with respect, do not step over it, when
	picking it up from the floor or placing it on the floor - lower your left knee and carefully
	handle or retrieve it. Do not bend over at the waist to reach down for it, especially if your butt
	is facing the people you are hoping to practice with. If you need to lean the shinai on the wall
	to free up floor space, always place the tip end up. No one wants to catch a shinai tip full of
	dust or something worse picked up from the floor. The shinai is the tool you will use to practice
	the techniques of how, where, and when to cut. It is also the weapon you wield to make successful
	strikes with and to fluster your opponent with – treat it well as it is not a toy.
</p>
<p>
	Even before we begin practice, perform adequate stretching and warm up. Everybody is different and
	changes in the weather effect all of us differently too. Be aware of these small differences and
	keep yourself in a safe limber state for practice.
</p>
<p>
	If you are unable to practice, but still able to attend - it is equally valuable to observe others
	practice (Mitori Geiko). Pay careful attention to compare what you see between different level
	players. Always show quiet consideration when watching practice. Banging around on the sidelines
	may cause a distraction and be dangerous to the practice participants. If you are watching from
	the side of the practice, always stay off of the wall. Leaning against a wall is considered very
	rude.
</p>
<p>
	If you are late, quietly prepare and stretch on your own. Be mindful of the restricted space to
	stretch under and join the practice for foot work drills or once line up is called. Jumping into
	the middle of stretching or subari is disruptive. If sensei or a senior member waves for you to
	join, then quickly join in.
</p>

<h2>About The Dojo</h2>
<p>
	The term Dojo comes from Buddhism meaning "place to study," which originally referred to the
	platform Buddha meditated on.
</p>
<p>
	Commit yourself to sincere training when entering the Dojo; really appreciate having a place to
	practice and people to practice with; maintain cleanliness; take care of your own belongings
	neatly (eg, shoes in a neat row outside of the practice area, clothes put away neatly and never
	laying in a pile).
</p>
<p>
	Feel obliged to leave your shoes in a neat row with the other members, far enough away from the
	entrance to allow easy entrance and exit from the practice room. There should never be a mountain
	or jumble of shoes right at the door entrance.
</p>
<p>
	Know the location of the shoumen. In other schools/dojos with a religious tradition, this may be
	referred to as the “kamiza” (Shinto) or “shizen” (Buddhist). Often times this was the location of
	a shrine. As we are a secular institution, we bow to the “shoumen” as a way of showing respect
	towards our practice and the tradition of Kendo. It has no religious significance. We have chosen
	specifically not to use “kamiza” or “shizen” so that our members may feel free to bring whatever
	religious tradition they choose to observe.
</p>

<h2>About Rei</h2>
<p>
	Rei is a way to show common courtesy, acknowledgement of others, and clearly convey that you are
	prepared to safely participate.
</p>
<p>
	Upon entry to the Dojo, enter then Rei. This is the way to pay respect to the Dojo and to mentally
	commit yourself to a sincere practice.
</p>
<p>
	There are other appropriate times to use Rei, and these will be discussed during your training and
	occasionally revisited in practice.
</p>
<p>
	When entering the practice space, we announce “Shitsurei shimasu”, and at leaving “Shitsurei
	shimashita”. This should be in a regular conversation voice as we are not looking to interrupt the
	harmony of the practice.
</p>

<h2>Basic Understandings</h2>
<p>
	Make it a habit to Seiza quietly at the beginning and the end.<br />
	Always do warming up and stretching before you do Kendo.<br />
	Be punctual and also prepared to lead – you never know when you will be given the chance to.<br />
	Always value the basics.<br />
	Be prepared to practice and understand you will repeat everything many, many, many times to develop
	muscle memory and grace.<br />
	Practice with someone formidable <br />
	Practice with proper attitude, courtesy and respect. <br />
	Grasp the value of watching; humbly learn merits of others by observing.<br />
	Make a commitment to Kendo and practice with initiative and determination.<br />
	Value graceful, elegant, and dignified Kendo.<br />
	Be prepared physically and mentally, then practice with eagerness<br />
	Know your capabilities and practice accordingly<br />
	Everything you see and do can be insightful teaching, always consider ways to improve.<br />
	Bring a fresh attitude into each practice, while being conscious of safety.<br />
	Be conscious of health and hygiene. <br />
	Do not overextend unreasonably, but always seek to push your boundries.<br />
	Winning in a match is a good thing, but it is not the most important thing in Kendo.<br />
	Instead consistently practice with the desire to learn to better understand yourself.<br />
	<strong>For students, consider how school work and Kendo can be balanced</strong>
</p>

