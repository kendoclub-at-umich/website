<h2>Benefits Of Kendo Training</h2>
<p>
	Physical conditioning and maturity of mind.<br />
	Etiquette, courtesy, morality, ethos, respect, and empathy.<br />
	Concentration, focus, attentiveness, and cultivation of an eagerness to learn.<br />
	Agility, dexterity, coordination.<br />
	Endurance, strength, perseverance.<br />
	Promptness, reliability, and accountability.<br />
	Orderliness and neatness.<br />
	Care of equipment and tools.<br />
</p>
