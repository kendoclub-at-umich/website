<h1>Things to Consider When Buying a Uniform</h1>

<p>
	This is a short buying guide intended for beginners who are considering purchasing a kendo
	uniform. Part of this guide also contains information specific to what our club considers
	appropriate. Please remember that standards vary between clubs and dojos, and that what is said
	here may not apply to your own situation.
</p>

<h2>Introduction:</h2>

<p>
	The uniform used in Kendo is made up of two parts, a coat-like top (“keiko- gi” or often
	abbreviated to “gi”) and a skirt like divided pants called a “hakama”. Several other martial arts
	(iaido, aikido, judo, kyudo) use similar uniforms or similar parts of the uniform as kendo.
</p>

<p>
	This guide is intended to show the differences between the uniforms across the martial arts, and
	the different options available between different kinds of kendo uniforms.
</p>

<p>
	This information is given to help beginners make a more informed decision when buying a uniform
	for kendo.
</p>

<h2>Similarities and differences between kendo uniforms and other martial arts uniforms.</h2>

<p>
	When shopping for a kendo uniform, you may see similar items for sale intended for iaido, kyudo,
	aikido, and judo. Beginners often have questions as to whether or not these uniforms are
	appropriate for use in kendo.
</p>

<p>
	Iaido uniforms are generally black and consist of a hakama and a gi top. The fabric used in the gi
	top tends to be much thinner than the gi top used in kendo. Although it is possible to wear an
	iaido uniform while practicing kendo, it is generally not recommended for safety reasons.
</p>

<p>
	Aikido uniforms are generally a white top and a black hakama. The major difference between an
	aikido hakama and a kendo hakama is the presence of a plastic “spoon” in the back of a kendo
	hakama. This “spoon” is used to hook the back of the hakama to the strings around the waist. An
	aikido hakama usually does not have this “spoon” because aikidoists take falls during their
	practice, thus hitting the “spoon” against their back. Also the aikido gi-tops tend to have longer
	length sleeves, as the kendo sleeve accommodates the kote.
</p>

<p>
	If you use an aikido uniform with a white gi-top, please be aware that blue do himo (strings) will
	tend to stain the uniform blue.
</p>

<p>
	Lighter uniforms such as those used in karate and taekwondo are generally not recommended past the
	beginner stage because they tend to be much thinner and provide less protection.
</p>

<p>
	Some modern kumdo hakamas come with Velcro attachments instead of the traditional straps used in
	kendo. Velcro strap hakamas are considered inappropriate for our club.
</p>

<p>
	Please be aware that once you are at the level of testing for a kendo rank, you must have an
	appropriate kendo uniform, as part of the testing grade depends on how well you wear the keikogi.
</p>

<h2>Factors in buying a keikogi and hakama.</h2>

<ul>
	<li>
		Color:

		<ul>
			<li>
				Hakama color:

				<ul>
					<li>
						Traditional hakamas generally come in either a black or dark navy color. Both black and
						dark navy are used in kendo, although dark navy is exclusively used in only in kendo.
						Either is considered appropriate for our club.
						<ul>
							<li>See note for blue hakamas in the next section.</li>
						</ul>
					</li>
					<li>
						Red, light blue, white, American flag print and ying-yang print hakamas are considered
						inappropriate for our club and should not be worn.
					</li>
				</ul>
			</li>

			<li>
				Keiko-gi color:

				<ul>
					<li>
						There are three traditional color/styles in keiko-gi tops, blue, white, and diamond
						pattern (Musashi style). Our club does not recognize any distinction between the styles
						or colors, so which color you get is up to you.
						<ul>
							<li>
								Blue keiko-gis and hakamas can either be dyed in a “natural” or “chemical” process.
								<ul>
									<li>
										Natural: A traditional indigo dye which is not colorfast and needs to be “set”
										prior to first use with a soak in a vinegar salt solution. Natural dyed items
										should only be washed by hand with very little detergent. These uniforms fade
										over time.
									</li>
									<li>
										Chemical: A newer process using a chemical dye that is colorfast and does not
										need to be “set” before first use. A chemical dye keikogi can be washed by
										machine on a cold cycle. If you are living in a dorm, you may want to consider
										getting a “chemically” dyed keikogi as it is easier to maintain.
									</li>
								</ul>
							</li>
							<li>
								White keikogi can be washed in a washing machine. They will show dirt and sweat
								faster than dark blue uniforms, and will stain blue from the bogu himo. However,
								white keikogi can be bleached periodically.
							</li>
							<li>
								Diamond pattern (Musashi). This is a white uniform with a black diamond pattern.
								Traditionally considered a children’s uniform, but recently this style has become
								more popular with adults.
							</li>
						</ul>
					</li>
				</ul>
			</li>
		</ul>
	</li>

	<li>
		Fabric

		<ul>
			<li>
				Keikogi

				<ul>
					<li>Keikogi are always made out of 100% cotton, and will shrink if dried in a dryer.</li>
					<li>
						Weight - Keikogi come in various weights referring to how much fabric is used in its
						construction. A double weight gi is considerably thicker and heavier than a single
						weight uniform. Though much more important in grappling arts such as judo, in kendo the
						weight of the uniform increases its heat retention. There is also a small increase in
						protection as the weight increases. The cost of the uniform also increases as the wieght
						of the fabric goes up.
					</li>
					<li>
						Thread count – some resellers market their products with thread count. In general
						quality as well as the price increases as the thread count goes up
					</li>
					<li>
						Special high performance fabrics – some high end keikogi have special high performance
						fabric liners which “wick” sweat and moisture away from the body. They are marketed
						under several different brand names such as “Fieldsensor”.
					</li>
				</ul>
			</li>
			<li>
				Hakama

				<ul>
					<li>
						Polyester (Tetron): Low maintenance, machine washable, not shrinkable and permanent
						press, so you won’t have to iron your pleats if they get wrinkled. Thinner and lighter
						than most cotton hakama. One disadvantage is that the Tetron doesn’t absorb sweat as
						well as cotton, so your legs get sweaty.
					</li>
					<li>
						100% cotton: High maintenance, can only be washed by hand, shrinkable, and gets wrinkled
						if not folded correctly. Thicker and heavier than Tetron and can absorb sweat much
						easier than Tetron. However, considered to be much more comfortable to wear.
					</li>
				</ul>
			</li>
		</ul>
	</li>
</ul>
