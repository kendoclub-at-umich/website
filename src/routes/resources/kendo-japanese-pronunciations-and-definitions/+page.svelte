	<dt>Senpai: 先輩 【せんぱい】</dt>
	<dd>senior</dd>

	<dt>Kouhai (Kohai): 後輩 【こうはい】</dt>
	<dd>junior</dd>

	<dt>Kenshi: 剣士 【けんし】</dt>
	<dd>a practitioner of kendo, fencer</dd>

	<dt>Kendoka: 剣道家【けんどうか】</dt>
	<dd>a practioner of kendo, fencer</dd>

	<dt>Senshu: 選手 【せんしゅ】</dt>
	<dd>player, competitor</dd>

	<dt>Shinpan: 審判 【しんぱん】</dt>
	<dd>referee</dd>

	<dt>Shinpanin: 審判員 【しんぱんいん】</dt>
	<dd>referee</dd>
</dl>
<p>
	Cultural note: the relationship between senior and junior is one aspect of kendo that establishes
	not only a hierarchy, but also a support structure. A 6th Dan and a 7th Dan will use these terms
	and relationships just as a beginner and ranking person might.
</p>

