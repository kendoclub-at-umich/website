<h1>Kendo Japanese Pronunciations and Definitions</h1>
<p>
	This document is intended to define and provide a basic pronunciation key for basic Japanese used
	during kendo practices. The many of our definitions are taken from Jim Breen’s World Wide Web
	Japanese Online Dictionary, which can be found at
	<a href="https://www.edrdg.org/cgi-bin/wwwjdic/">https://www.edrdg.org/cgi-bin/wwwjdic/</a>, and
	also from the All U.S. Kendo Federation Website at
	<a href="https://www.auskf.org/info/what-is-kendo">https://www.auskf.org/info/what-is-kendo</a>.
	They are great resources.
</p>
<p>
	Disclaimer: We are not professional linguists, so this document may (or most likely) contain
	errors or information which is not <em>entirely</em> accurate. Please use the following document as
	a light and entertaining guide to help bridge the language barrier, not as a definitive source.
</p>

<h2>A few points about the Japanese language</h2>
<p>
	Japanese, unlike English, contains many more words which are homonyms, or words that are
	pronounced the same but have multiple meanings. This is typically a source of confusion for novice
	Japanese speakers and native Japanese speakers alike. If you try to find definitions of kendo
	terms on your own, please keep that in mind.
</p>
<p>As an example, here are three words pronounced “kendo” with different meanings:</p>
<dl>
	<dt>Kendo: 権道 【けんどう】</dt>
	<dd>inappropriate means to a worthy end; expediency</dd>
	<dt>Kendo: 県道 【けんどう】</dt>
	<dd>prefectural road</dd>
	<dt>Kendo: 剣道 【けんどう】</dt>
	<dd>kendo; swordsmanship; fencing</dd>
</dl>
<p>Here's an amusing non-kendo example:</p>
<dl>
	<dt>Niwa niwa niwa niwatori ga imasu: 庭には二羽鶏がいます.</dt>
	<dd>In the backyard there are two chickens.</dd>
</dl>

<h2>A few words about pronunciation</h2>
<p>
	In general all of the romanized (romaji) versions of Japanese letters are pronounced as written,
	with one exception. The letter を is written in roman characters as “wo”, as in “kiwotsuke” but is
	pronounced more often as “o” to sound more like “ki-o-tsuke” or “kyotsuke”, identical to the お
	“o” sound in “otagai”. It is written in roman characters as “wo” to differentiate the two letters
	from each other. In some cases the use of “wo” is optional and denotes a slightly more formal
	tone, while its omission denotes more of an imperative command. In cases where the use of “wo” is
	optional, it is indicated by parentheses. Many times you will see the character of は (“ha”)
	written, but if it is used outside of a word it is pronounced “wa” like the character わ. In
	addition, if the character へ is used in a word it is pronounced “hey”, while if it is used
	outside of a word in a sentence it is pronounced “eh” identical to え.
</p>
<p>
	Many written words in common usage drop the unemphasized and sometimes silent “u” う. Two examples
	are “bougu” (bogu) and “kyuu” (kyu). Sometimes it is easier for nonJapanese speakers to think of
	these “silent” characters as a sign to hold the vowel or consonant a little longer.
</p>
<p>
	For ease of pronunciation and computerized input systems, the phonetic romanization (Hepburn
	system) is used in this glossary. The complete roman spelling is first, followed by the common
	spelling in parentheses if different from the phonetic version. The kanji follow the romanized
	version of the word, and the hiragana phonetic characters are last.
</p>
<p>A pronunciation guide follows the glossary if you feel like you need more information.</p>

<h2>A brief note about dialects:</h2>
<p>
	There are about one hundred dialects of Japanese, including those that were constructed
	deliberately. They range from slight differences in pronunciation to differences so pronounced
	that the dialect is unintelligible for other Japanese speakers from other parts of the country.
	Please keep this in mind as the terminology from school to school and individual instructors may
	be different from what is listed here.
</p>

<h2>A few words about politeness:</h2>
<p>
	Japanese has many levels of formality. The ritualized language used within Kendo practice is more
	formal and polite than everyday language. The general rule is that the longer the phrase, the more
	polite it is.
</p>
<p>
	As an example, the following phrases all mean “Thank you”, the English translation roughly
	approximates the level of formality implied by their usage:
</p>
<dl>
	<dt>Doumo (Domo): どうも</dt>
	<dd>thanks (informal)</dd>

	<dt>Arigato: 有難う【ありがとう】</dt>
	<dd>thanks (informal)</dd>

	<dt>Doumo arigato: どうも有難う 【どうもありがとう】</dt>
	<dd>thank you (formal)</dd>

	<dt>Arigato gozaimasu: 有難うご座います【ありがとうございます】</dt>
	<dd>thank you (formal)</dd>

	<dt>Doumo arigato gozaimasu: どうも有難うご座います【どうもありがとうございます】</dt>
	<dd>thank you (very much) (formal+)</dd>
</dl>
<p>Notice that the phrase used during kendo practice is the “formal+” version.</p>

