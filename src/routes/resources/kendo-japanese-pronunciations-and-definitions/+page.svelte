<h3>Warm up exercises:</h3>
<dl>
	<dt>Taisou (Taiso): 体操 【たいそう】</dt>
	<dd>exercise, warm up exercises</dd>

	<dt>Suburi: 素振り 【すぶり】</dt>
	<dd>
		a cut or stretching movement with a (wooden/bamboo) sword, usually from a starting position
		above one's head.
	</dd>

	<dt>Jyouge suburi (Jyoge suburi) 上下素振り 【じょうげすぶり】</dt>
	<dd>high to low, up and down cuts</dd>

	<dt>Renzoku sayuu suburi: 連続左右素振り【れんぞくさゆうすぶり】</dt>
	<dd>
		continuous left and right. Often abbreviated as “sayuu-suburi” 左右素振り【さ ゆうすぶり】
	</dd>

	<dt>Katate suburi: 片手素振り 【かたてすぶり】</dt>
	<dd>one handed</dd>

	<dt>Yoko-men suburi: 横面素振り【よこめんすぶり】</dt>
	<dd>to the sides of the men</dd>

	<dt>Matawari suburi: 股割り素振り【またわりすぶり】</dt>
	<dd>
		(lit. thigh splitting cut). Feet placed outside of shoulder width, with toes pointing at a 45
		degree angle. This stretch starts as a big men cut and finishes at kote height, while squatting
		during the downward cut. An upright posture thoughout the stretch is key.
	</dd>
</dl>

